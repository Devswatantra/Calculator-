#sample calculator
a=int(input("Enter the number: "))
b=int(input("Enter the number: "))
character=input()
if character=='+' :
    print(a+b)
elif character=="-" :
    print(a-b)
elif character=="*" :
    print(a*b)
elif character=="/" :
    print(a/b)
elif character=="%" :
    print(a%b)
